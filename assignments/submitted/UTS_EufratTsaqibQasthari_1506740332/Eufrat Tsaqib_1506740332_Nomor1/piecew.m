%%file piecew.m
function y = piecew(t);

if t >= 0 && t < 15
    y = (38.1454 * t) + (0.31743 * (t^3));
elseif t >= 15 && t <= 33
    y = 1036 + (130.909 * (t-15)) + (6.18425 * (t-15)^2) - (0.428 * (t-15)^3);
elseif t > 33  
    y = 2900 - (62.468 * (t-33)) - (16.9274 * (t-33)^2) - (0.41796 * (t-33)^3);
else
    y = 0;
end